# How to access a data from one range to another in lists
# Syntax : list_name[m:n:o]

names = ["pablo", "chunnu", "munnu", "tappu", "samosa singh"]
print(names[1:4:1])

#from munnu to samosa
print(names[2:5:1])

length = 5
print(names[2:length:1])
print(names[2:])
print(names[:3])
print(names[0:5:2])